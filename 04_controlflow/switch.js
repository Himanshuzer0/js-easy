
const month = "march"

switch (month) {
    case "jan" :
        console.log("January");
        break;

    case "feb" : 
    console.log("Febuary");
    break ;

    case "march" : 
    console.log("March");
    break ;

    case "apr" : 
    console.log("April");
    break ;

    default  :
    console.log("default value print");
    break;
}